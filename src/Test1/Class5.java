package Test1;

public class Class5 {
	public static void main(String[] args) {
		
		int x=5;
		int y=3;
		
		x+=y; // x=x+y=8
		System.out.println("X :"+x);
		
		x-=y; // x=x-y=5
		System.out.println("X :"+x);
		
		x*=y; // x=x*y=15
		System.out.println("X :"+x);
		
		x/=y; // x=x/y=5
		System.out.println("X :"+x);
		 
		x%=y; // x=x%y=2
		System.out.print("X :"+x);
		
	}
}
