package Test1;

public class Class__5 {
	public static void main(String[] args) {
		
		int x=25;
		int y;
		
		y= ++x; // prefix increment
		System.out.println("Y="+ y);
		
		y= x++; // postfix increment
		System.out.println("Y=" + y);
		
		y= --x; // prefix decrement
		System.out.println("Y="+y);
		
		y= x--; // postfix decrement
		System.out.print("Y="+y);
		
	}
}






