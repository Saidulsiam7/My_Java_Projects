package Test1;

public class BitwiseOperator {
	
	public static void main(String[] args) {
		
		int a = 32;
		int b = 12;
		
		int C;
		
		C = a&b;
		System.out.println(" a & b ="+C);
		
		C = a|b;
		System.out.println(" a | b ="+C);
		
		C = a ^ b;
		System.out.println(" a ^ b ="+C);
		
	}

}

